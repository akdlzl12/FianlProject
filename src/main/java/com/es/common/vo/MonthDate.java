package com.es.common.vo;

import java.util.Date;
import java.util.List;

public class MonthDate {

	private List<Date> startDate;
	private List<Date> endDate;
	private String threeMonthDate;
	private String twoMonthDate;
	private String oneMonthDate;
	
	public MonthDate() {}

	public List<Date> getStartDate() {
		return startDate;
	}

	public void setStartDate(List<Date> startDate) {
		this.startDate = startDate;
	}

	public List<Date> getEndDate() {
		return endDate;
	}

	public void setEndDate(List<Date> endDate) {
		this.endDate = endDate;
	}

	public MonthDate(List<Date> startDate, List<Date> endDate) {
		super();
		this.startDate = startDate;
		this.endDate = endDate;
	}

	public String getThreeMonthDate() {
		return threeMonthDate;
	}

	public void setThreeMonthDate(String threeMonthDate) {
		this.threeMonthDate = threeMonthDate;
	}

	public String getTwoMonthDate() {
		return twoMonthDate;
	}

	public void setTwoMonthDate(String twoMonthDate) {
		this.twoMonthDate = twoMonthDate;
	}

	public String getOneMonthDate() {
		return oneMonthDate;
	}

	public void setOneMonthDate(String oneMonthDate) {
		this.oneMonthDate = oneMonthDate;
	}

	@Override
	public String toString() {
		return "MonthDate [startDate=" + startDate + ", endDate=" + endDate + ", threeMonthDate=" + threeMonthDate
				+ ", twoMonthDate=" + twoMonthDate + ", oneMonthDate=" + oneMonthDate + "]";
	}

	

	
	
	

		
	
	
	
}
